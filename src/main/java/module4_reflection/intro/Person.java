package module4_reflection.intro;

/**
 * Класс для анализа через рефлексию.
 * Содержит поля, методы и конструкторы разных типов.
 */
public class Person {

    // === ПОЛЯ КЛАССА ===

    /**
     * Приватное поле для хранения имени.
     * Модификатор 'private' означает, что это поле доступно только внутри класса Person.
     */
    private String name;

    /**
     * Приватное поле для хранения возраста.
     * Тип 'int' — это примитивный целочисленный тип.
     */
    private int age;

    /**
     * Приватное поле для хранения адреса электронной почты.
     * Может быть null, если email не указан.
     */
    private String email;

    /**
     * Публичное статическое поле для хранения названия компании.
     * Модификатор 'public' означает, что это поле доступно из любого места.
     * Модификатор 'static' означает, что это поле принадлежит классу, а не экземпляру.
     * Модификатор 'final' означает, что значение нельзя изменить после инициализации.
     */
    public static final String COMPANY = "Skillbox";

    // === КОНСТРУКТОРЫ ===

    /**
     * Конструктор без параметров.
     * Создаёт объект Person с пустыми значениями полей.
     * Модификатор доступа 'public' позволяет создавать объекты из любого места.
     */
    public Person() {
        // Пустой конструктор — поля останутся со значениями по умолчанию:
        // name = null, age = 0, email = null
    }

    /**
     * Конструктор с двумя параметрами.
     * Создаёт объект Person с указанными именем и возрастом.
     *
     * @param name Имя человека
     * @param age Возраст человека
     */
    public Person(String name, int age) {
        // this.name — обращение к полю класса 'name'
        // name — параметр конструктора (локальная переменная)
        // Ключевое слово 'this' используется для различения поля и параметра с одинаковым именем
        this.name = name;
        this.age = age;
        // Поле 'email' остаётся null
    }

    /**
     * Конструктор с тремя параметрами.
     * Создаёт объект Person со всеми указанными данными.
     *
     * @param name Имя человека
     * @param age Возраст человека
     * @param email Адрес электронной почты
     */
    public Person(String name, int age, String email) {
        this.name = name;
        this.age = age;
        this.email = email;
    }

    // === ГЕТТЕРЫ (методы для получения значений полей) ===

    /**
     * Метод для получения имени.
     *
     * @return текущее значение поля 'name'
     */
    public String getName() {
        return name; // Возвращаем значение поля 'name'
    }

    /**
     * Метод для получения возраста.
     *
     * @return текущее значение поля 'age'
     */
    public int getAge() {
        return age; // Возвращаем значение поля 'age'
    }

    /**
     * Метод для получения адреса электронной почты.
     *
     * @return текущее значение поля 'email'
     */
    public String getEmail() {
        return email; // Возвращаем значение поля 'email'
    }

    // === СЕТТЕРЫ (методы для установки значений полей) ===

    /**
     * Метод для установки имени.
     *
     * @param name новое значение для поля 'name'
     */
    public void setName(String name) {
        this.name = name; // Устанавливаем значение поля 'name'
    }

    /**
     * Метод для установки возраста.
     *
     * @param age новое значение для поля 'age'
     */
    public void setAge(int age) {
        this.age = age; // Устанавливаем значение поля 'age'
    }

    /**
     * Метод для установки адреса электронной почты.
     *
     * @param email новое значение для поля 'email'
     */
    public void setEmail(String email) {
        this.email = email; // Устанавливаем значение поля 'email'
    }

    // === ДОПОЛНИТЕЛЬНЫЕ МЕТОДЫ ===

    /**
     * Метод для вывода информации о человеке.
     * Возвращает строку с данными о человеке.
     *
     * @return строка в формате "Person{name='...', age=..., email='...'}"
     */
    @Override
    public String toString() {
        // Используем конкатенацию строк для формирования результата
        // '\'' — это экранированный символ одинарной кавычки
        return "Person{name='" + name + "', age=" + age + ", email='" + email + "'}";
    }

    /**
     * Приватный метод для внутреннего использования.
     * Проверяет, является ли человек совершеннолетним.
     * Модификатор 'private' означает, что этот метод доступен только внутри класса Person.
     *
     * @return true, если возраст >= 18, иначе false
     */
    private boolean isAdult() {
        return age >= 18; // Возвращаем результат сравнения
    }

    /**
     * Метод для поздравления с днём рождения.
     * Увеличивает возраст на 1.
     */
    public void celebrateBirthday() {
        age++; // Инкремент — увеличение значения на 1
        System.out.println(name + " теперь " + age + " лет!");
    }
}